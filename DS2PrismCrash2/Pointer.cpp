Pointer::Pointer(DWORD32* offsets, size_t length) :
	offsets(offsets), length(length)
{
	
}

Pointer::Pointer(const Pointer& p)
{
	length = p.length;
	offsets = new DWORD32[p.length];
	memcpy(offsets, p.offsets, p.length * 4);
}

Pointer::~Pointer()
{
	delete[] offsets;
}

